def insert_data():
    with app.app_context():
        db.create_all()

        # 插入店铺
        shop_map = {}
        added_shops = 0
        for canteen, name, location_desc in SHOPS:
            existing = Shop.query.filter_by(canteen=canteen, name=name).first()
            if existing:
                shop_map[(canteen, name)] = existing
                continue
            shop = Shop(canteen=canteen, name=name, location_desc=location_desc)
            db.session.add(shop)
            db.session.flush()
            shop_map[(canteen, name)] = shop
            added_shops += 1

        db.session.commit()
        print(f"✅ 店铺：新增 {added_shops} 个，共 {len(SHOPS)} 个窗口")

        # 插入帖子
        added_posts = 0
        devices = [f"demo_device_{i}" for i in range(1, 10)]
        base_time = datetime.utcnow() - timedelta(days=7)

        for i, (text, shop_idx, dish_name, quote, tags_str) in enumerate(POSTS):
            # 用帖子内容查重
            existing = Post.query.filter_by(text=text).first()
            if existing:
                continue

            shop = None
            if shop_idx is not None and shop_idx < len(SHOPS):
                canteen, name, _ = SHOPS[shop_idx]
                shop = shop_map.get((canteen, name))

            # 时间分散在过去7天内
            created_at = base_time + timedelta(
                hours=random.randint(0, 168),
                minutes=random.randint(0, 59)
            )

            post = Post(
                device_id=devices[i % len(devices)],
                text=text,
                shop_id=shop.id if shop else None,
                dish_name=dish_name,
                quote=quote,
                tags=tags_str,
                created_at=created_at
            )
            db.session.add(post)
            added_posts += 1

        db.session.commit()
        print(f"✅ 帖子：新增 {added_posts} 条，共 {len(POSTS)} 条")

        # 统计
        total_shops = Shop.query.count()
        total_posts = Post.query.count()
        print(f"\n📊 数据库现状：{total_shops} 个窗口，{total_posts} 条帖子")
        print("\n🎉 数据导入完成！重启 python app.py 后刷新页面即可看到数据。")


if __name__ == '__main__':
    insert_data()
